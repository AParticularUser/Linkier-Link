unsafe extern "C" fn attack_status_main(agent: &mut L2CFighterCommon) -> L2CValue {
    agent.status_Attack();
    VarModule::off_flag(agent.module_accessor, status::LINK_FLAG_ATTACK_IS_BUFFERD);
    agent.sub_shift_status_main(L2CValue::Ptr(attack_status_main_loop as *const () as _))
}
pub unsafe fn attack_status_main_loop(agent: &mut L2CFighterCommon) -> L2CValue {
    if MotionModule::motion_kind(agent.module_accessor) == hash40("attack_13") 
    && !CancelModule::is_enable_cancel(agent.module_accessor) {
        if ControlModule::check_button_trigger(agent.module_accessor, *CONTROL_PAD_BUTTON_ATTACK) 
        && !VarModule::is_flag(agent.module_accessor, status::LINK_FLAG_ATTACK_IS_BUFFERD) {
            VarModule::on_flag(agent.module_accessor, status::LINK_FLAG_ATTACK_IS_BUFFERD);
        }
        let mut advance_attack = false;
        if WorkModule::is_flag(agent.module_accessor, *FIGHTER_STATUS_ATTACK_FLAG_ENABLE_COMBO) 
        && (
            (
                AttackModule::is_infliction_status(agent.module_accessor, *COLLISION_KIND_MASK_HIT)
                && ControlModule::check_button_on(agent.module_accessor, *CONTROL_PAD_BUTTON_ATTACK)
            )
            || VarModule::is_flag(agent.module_accessor, status::LINK_FLAG_ATTACK_IS_BUFFERD)
        ) {
            advance_attack = true;
        }else if WorkModule::is_flag(agent.module_accessor, *FIGHTER_STATUS_ATTACK_FLAG_ENABLE_NO_HIT_COMBO) 
        && ControlModule::check_button_on(agent.module_accessor, *CONTROL_PAD_BUTTON_ATTACK) {
            advance_attack = true;
        }
        if advance_attack {
            WorkModule::off_flag(agent.module_accessor, *FIGHTER_STATUS_ATTACK_FLAG_CONNECT_COMBO);
            WorkModule::off_flag(agent.module_accessor, *FIGHTER_STATUS_ATTACK_FLAG_ENABLE_COMBO);
            WorkModule::off_flag(agent.module_accessor, *FIGHTER_STATUS_ATTACK_FLAG_ENABLE_NO_HIT_COMBO);
            MotionModule::change_motion(agent.module_accessor, Hash40::new("attack_14"), 0.0, 1.0, false, 0.0, false, false);
        }
    }
    agent.status_Attack_Main()
}
